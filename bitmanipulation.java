import java.util.*;
import java.lang.*;
public class bitmanipulation
{
    public static void main(String[] args) {
        System.out.println(5<<2);
        System.out.println(5>>2);
        System.out.println(5&4);
        System.out.println(5|4);
        System.out.println(5^4);
        System.out.println(~5);
    }
}